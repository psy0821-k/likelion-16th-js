// 1. 함수 선언문
function logger(nth) {
  // 암묵적으로 undefined 반환
  // return

  // 명시적으로 결과 값 반환
  return '멋사 프론트엔드 부트캠프 ' + parseInt(nth, 10) + '기 여러분 화이팅!'
}

// 함수 실행(사용, 호출)
console.log(logger('16기'))
console.log(logger(14))
console.log(logger(12))

// 함수 실행(사용, 호출)
console.log(logger('16기', '여러분 힘내세요! 💪'))
console.log(logger(14, '멋진 개발자되세요! 😊'))
console.log(logger(12, '월급 많이 받으세요~ 🍀'))


// 2. 함수 표현식
// const 변수 = 함수(값)
const myLogger = function(nth, message) {
  let resultMessage = ''
  resultMessage += '멋사 ' + parseInt(nth, 10) + '기 여러분!'
  resultMessage += ' '
  resultMessage += message
  resultMessage += '🥳'

  return resultMessage
}

console.log(myLogger(16, '오늘도 행복하세요!'))

